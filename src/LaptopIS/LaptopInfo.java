    public void ImportCSVfile(){
        errorFound=false;
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Specify a file to Open");

        int userSelection = fileChooser.showDialog(this, "Open");
        if(userSelection == fileChooser.APPROVE_OPTION){
            File filetosave = fileChooser.getSelectedFile();
            
            try {
                FileReader fr = new FileReader(filetosave);
                BufferedReader br = new BufferedReader(fr);
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                Object[] lines = br.lines().toArray();
                deleteTable();
                Device newDevice;
                
                for(int i = 1; i<lines.length;i++){
                    String[] row = lines[i].toString().split(",");
                    model.addRow(row);

                    String Model_Number= row[0].trim();
                    String Laptop_Name = row[1].trim();
                    String Company = row[2].trim();
                    String Laptop_Type = row[3].trim();
                    String Laptop_Series = row[4].trim();
                    String Processor = row[5].trim();
                    String Price_range= row[6].trim();
                    int Price= Integer.parseInt(row[7].trim());
                    //System.out.println(Model_Number.trim() +" "+ Laptop_Name.trim() +" "+ Company.trim() +" "+ Laptop_Type.trim() +" "+ Laptop_Series.trim() +" "+ Processor.trim() +" "+ Price_range.trim() +" "+ Price);
                    newDevice = new Device(Model_Number, Laptop_Name, Company, Laptop_Type, Laptop_Series, Processor, Price_range, Price);
                    deviceArrayList.add(newDevice);

                }
            } catch (Exception e) {
                System.err.println(e);
                JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
                errorFound=true;
            }

        }
            
        if(tableEmpty()==true && errorFound==false){
            JOptionPane.showMessageDialog(null, "Table Updated", "CSV File Imported",JOptionPane.INFORMATION_MESSAGE);
        } else if(tableEmpty()==true){
            JOptionPane.showMessageDialog(null, "Current Data Override and Table Updated", "CSV File Imported", JOptionPane.INFORMATION_MESSAGE);
        }

    }
