    public void deleteTable(){
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        deviceArrayList.removeAll(deviceArrayList);
    }
