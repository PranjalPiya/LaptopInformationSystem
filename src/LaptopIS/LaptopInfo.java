    public void ImportFile(){
        if(errorFound == false){
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Specify a file to Open");
            int userSelection = fileChooser.showDialog(this, "Open");
            if(userSelection == fileChooser.APPROVE_OPTION){
                File filetosave = fileChooser.getSelectedFile();
            try {
                FirstEntry=false;
                FileReader fr = new FileReader(filetosave);
                BufferedReader br = new BufferedReader(fr);
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                Object[] lines = br.lines().toArray();
                deleteTable();
                Device newDevice;
                for(int i = 0; i<lines.length;i++){
                    String[] row = lines[i].toString().split(", ");
                    model.addRow(row);
                    
                    String Model_Number= row[0];
                    String Laptop_Name = row[1];
                    String Company = row[2];
                    String Laptop_Type = row[3];
                    String Laptop_Series = row[4];
                    String Processor = row[5];
                    String Price_range= row[6];
                    int Price= Integer.parseInt(row[7]);
                    
                    newDevice = new Device(Model_Number, Laptop_Name, Company, Laptop_Type, Laptop_Series, Processor, Price_range, Price);
                    deviceArrayList.add(newDevice);
                    
                }
                errorFound = true;
            } catch (Exception e) {
            }
        }else{
            JOptionPane.showMessageDialog(null, "You Have Already loaded the same File", "Error!!", JOptionPane.ERROR_MESSAGE);
        }
    }}
